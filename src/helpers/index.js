export const capitalLetter = str => {
  return str.charAt(0).toUpperCase() + str.slice(1) 
}

export const dmAcm = height => {
  let m = height/10;
  return m
}

export const hgAkg = weight => {
  let kg = weight * 0.1
  return kg
}

export const mayusculas = (str) => {
  return str.toUpperCase()
} 